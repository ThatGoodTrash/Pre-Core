#include <stdio.h>

int main()
{
    char *recipe = 
    "Cookie Cake Recipe\n" \
    "Ingredients: \n" \
    "3/4 cup (170g) unsalted butter, softened to room temperature \n" \
    "1 cup (200g) packed light or dark brown sugar \n" \
    "1 large egg + 1 egg yolk* \n" \
    "2 teaspoons pure vanilla extract \n" \
    "2 cups (250g) all-purpose flour (spoon & leveled) \n" \
    "2 teaspoons cornstarch (aka cornflour)* \n" \
    "1 teaspoon baking soda \n" \
    "1/2 teaspoon salt \n" \
    "1 and 1/2 cup (225g) semi-sweet chocolate chips \n" \
    "optional: chocolate buttercream for topping \n" \
    "Directions \n" \
    "Preheat oven to 350°F (177°C). Spray a 9-inch pie dish or cake pan with nonstick spray. Set aside. \n" \
    "In a large bowl using a hand-held mixer or stand mixer fittedwith paddle attachment, beat the butter for 1 minute on medium speed until completely smooth and creamy. \n" \
    "Add the brown sugar and beat on medium speed until creamy, about 1 minute. Mix in egg, egg yolk, and vanilla on medium-high speed until combined. Scrape down the sides and bottom of the bowl as needed. \n" \
    "In a separate bowl, combine flour, cornstarch, baking soda and salt. On low speed, slowly mix into the wet ingredients until combined. The cookie dough will be quite thick. \n" \
    "Add 1 and 1/4 cups chocolate chips and mix for about 5 seconds until evenly disbursed. \n" \
    "Press the cookie dough evenly into the prepared pan. Bake for 20-25 minutes or until the cake is lightly golden brown (could be up to 30 minutes, depending on your oven).\n" \
    "Remove from the oven and set the pan on a wire rack to cool completely.\n" \
    "Once cooled, use a sharp knife or metal spatula to loosen the sides of the cookie cake from the pan and transfer to a serving dish. (Or use can serve directly from the pan.) \n" \
    "The chocolate buttercream is optional. Instead, decorate with whipped cream or top with vanilla ice cream. \n" \
    "Decorate the cooled cookie cake with frosting and remaining 1/4 cup chocolate chips. Depending how much frosting you use, you may have some leftover.  \n" \
    "Cookie cake remains fresh covered tightly at room temperature for up to 3 days.\n";

    printf("%s", recipe);
}